package multicalc;

public class Scalar 
{
	private double i;
	private double j;
	private double k;
	
	
	public void setIscalar( int i )
	{
	  this.i = i;
	}
	
	public void setJscalar( int j )
	{
	   this.j = j;
	}
	
	public void setKscalar( int k )
	{
       this.k = k;		
	}
	
	public double getI()
	{
	   return i;
	}
	
	public double getJ()
	{
	   return j;
	}
	
	public double getK()
	{
	   return k;
	}

}
